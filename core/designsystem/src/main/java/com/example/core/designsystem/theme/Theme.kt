@Composable
fun AdventAITheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme
    val statusColors = if (darkTheme) DarkStatusColors else LightStatusColors

    CompositionLocalProvider(LocalAdventStatusColors provides statusColors) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = AdventTypography,
            content = content
        )
    }
}

/**
 * Доступ к расширенным токенам дизайн-системы: `AdventTheme.status`, `AdventTheme.spacing`,
 * `AdventTheme.radii`. Цвета и типографику по-прежнему берём из [MaterialTheme].
 */
object AdventTheme {
    val status: AdventStatusColors
        @Composable
        @ReadOnlyComposable
        get() = LocalAdventStatusColors.current

    val spacing: AppSpacing get() = AppSpacing
    val radii: AppRadii get() = AppRadii
}
